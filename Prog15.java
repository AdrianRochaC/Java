//Este programa imprime la tabla del 4 con FOR
import java.io.*;
public class Prog15
{
    public static void main (String args[])
    {
        int n,a;
        n=0;
        a=0;
        for (n=1; n<=10; n++)
        {
            a=n*4;
            System.out.println(""+4+"   "+n+"   "+a);
        }
    }
    
}
