//Este programa imprime la tabal del 4 con while
import java.io.*;
public class Prog13 
{
    public static void main(String args[])
    {
        int n,a;
        n=0;
        a=0;
        while (n<10)
        {
            n++;
            a=4*n;
            System.out.println(" "+4+"   "+n+"    "+a);
        }
    }
    
}
